#include "minishell.h"

int
	shouldescape(int c1, int c2, int qt)
{
	return (qt != '\'' && c1 == '\\' && !(qt == '\"' && !isspecialchar(c2)));
}

int
	shouldexpand(int c1, int c2, int qt)
{
	return (qt != '\'' && c1 == '$' && c2);
}

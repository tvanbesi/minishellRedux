#include "minishell.h"

int
	shouldescape(int c1, int c2, int q)
{
	return (c1 == '\\' && (!q || (q == '\"' && (c2 == '$' || isquote(c2)))));
}

int
	shouldexpand(int c1, int c2, int q)
{
	return (c1 == '$'
	&& ((q != '\'' && (ft_isalnum(c2) || c2 == '_' || c2 == '?'))
	|| (!q && c2 == '\"')));
}
